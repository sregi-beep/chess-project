def _square_index(square: int, is_white: bool) -> int:
    """Map a chess square to the piece-square table index.
    Tables are from white's perspective (a1=0, h8=63 with rank 8 at top).
    For black pieces, mirror vertically.
    """
    rank = chess.square_rank(square)  # 0=rank1, 7=rank8
    file = chess.square_file(square)  # 0=a, 7=h
    if is_white:
        # table row 0 = rank 8 (top), so mirror rank
        row = 7 - rank
    else:
        row = rank
    return row * 8 + file


def basic_eval(board: chess.Board) -> int:
    """Return evaluation in centipawns from the perspective of the side to move.
    Positive = good for side to move, negative = bad.
    """
    if board.is_checkmate():
        return -100000  # side to move is in checkmate = losing
    if board.is_stalemate() or board.is_insufficient_material():
        return 0

    score = 0
    for piece_type in PIECE_VALUES:
        table = PIECE_TABLES.get(piece_type)
        for square in board.pieces(piece_type, chess.WHITE):
            bonus = table[_square_index(square, True)] if table else 0
            score += PIECE_VALUES[piece_type] + bonus
        for square in board.pieces(piece_type, chess.BLACK):
            bonus = table[_square_index(square, False)] if table else 0
            score -= PIECE_VALUES[piece_type] + bonus

    # Return from side-to-move perspective
    return score if board.turn == chess.WHITE else -score
